// *********************************************************************************
// *****	UV_Thread														********
// *****	Continuous thread that reads UV Sensor							********
// *****	return: void													********
// *****	(1) void pointer												********
// *********************************************************************************
void *UV_Thread(void *ptr)
{
// *********************************************************************************
// *****	Initialize Variables											********
	uv_struct strc_temp_uv;
// ----------  END  ----------------------------------------------------------------

// *********************************************************************************
// *****	Configure UV Sensor ADC											********
	//adc_config(v_device,UV_ADDRESS,0x03,0x00);
// ----------  END  ----------------------------------------------------------------
	
// *********************************************************************************
// *****	Main Loop of UV_Thread											********
	for(;;)
	{
		strc_temp_uv.v_sen_volt = adc_read(v_device,UV_ADDRESS);
		Calculate_UV(&strc_temp_uv);
		strc_uv = strc_temp_uv;
		new_uv_data = 1;
		usleep(500000);
	}
// ----------  END  Main UV_Thread Loop  -------------------------------------------
}
// ----------  END  UV_Thread  -----------------------------------------------------



// *********************************************************************************
// *****	Get_UV_Data														********
// *****	this function is used to get the data out of the 				********
// *****	UV.c file and back to the calling routine						********
// *****	(r) void														********
// ***** 	(1) uv_struct *: pointer to a uv_struct 						********
// *********************************************************************************
void Get_UV_Data(uv_struct *temp)
{
	*temp = strc_uv;		// ***** copy data into tmp struct
	new_uv_data = 0;		// ***** set new data flag
}
// ----------  END GET_ALT_DATA  ---------------------------------------------------

// *********************************************************************************
// *****	Save_I2C_device_ID												********
// *****	this functions receives and saves the I2C device ID number		********
// *****	(r) void														********
// ***** 	(1) int: device ID									            ********
// *********************************************************************************
void save_uv_i2c_device_id(int I2C_ID)
{
	v_device = I2C_ID;
	return;
}
// ----------  END SAVE_I2C_DEVICE_ID ----------------------------------------------

// *********************************************************************************
// *****	Calculate_UV													********
// *****	This functions calculates uv									********
// *****	(r) void														********
// ***** 	(1) uv_struct *: pointer to uv structure  		   				********
// *********************************************************************************
void Calculate_UV(uv_struct *temp)
{
	temp->v_UV = (double)((UV_M*temp->v_sen_volt + UV_B)*UV_SCALE);
	return;
}
